package dh.project11.packs.admin;

public class UpdateSkinsVo {
	
	private String skins_version;
	private String skins_cid;
	private String skins_id;
	private int skins_num;
	private String skins_name;
	
	public String getSkins_version() {
		return skins_version;
	}
	public void setSkins_version(String skins_version) {
		this.skins_version = skins_version;
	}
	public String getSkins_cid() {
		return skins_cid;
	}
	public void setSkins_cid(String skins_cid) {
		this.skins_cid = skins_cid;
	}
	public String getSkins_id() {
		return skins_id;
	}
	public void setSkins_id(String skins_id) {
		this.skins_id = skins_id;
	}
	public int getSkins_num() {
		return skins_num;
	}
	public void setSkins_num(int skins_num) {
		this.skins_num = skins_num;
	}
	public String getSkins_name() {
		return skins_name;
	}
	public void setSkins_name(String skins_name) {
		this.skins_name = skins_name;
	}
}
